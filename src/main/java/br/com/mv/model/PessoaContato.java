package br.com.mv.model;

public class PessoaContato {
	
	private int id;
	private String nome;
	private String cpf;
	private int idade;
	private int quant;
	
	public PessoaContato(int id, String nome, String cPF, int idade, int quant) {
		super();
		this.id = id;
		this.nome = nome;
		cpf = cPF;
		this.idade = idade;
		this.quant = quant;
	}
	

}
